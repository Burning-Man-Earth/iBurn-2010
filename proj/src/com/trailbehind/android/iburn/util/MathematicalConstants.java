package com.trailbehind.android.iburn.util;

/**
 * The Class MathematicalConstants.
 */
public class MathematicalConstants {

    /**
     * Instantiates a new mathematical constants.
     */
    private MathematicalConstants() {
    }

    /** The Constant PI. */
    static final public float PI = (float) (Math.PI);

    /** The Constant PI_HALF. */
    static final public float PI_HALF = PI / 2;

    /** The Constant PI_FURTH. */
    static final public float PI_FURTH = PI / 4;

    /** The Constant DEG2RAD. */
    public static final float DEG2RAD = (float) (Math.PI / 180.0);
    
    /** The Constant RAD2DEG. */
    public static final float RAD2DEG = (float) (180.0 / Math.PI);
}
